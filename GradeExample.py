grade = int(input("Score (100) : "))

if grade >= 80:
    print("Grade A")
elif grade >= 75:
    print("Grade B+")
elif grade >= 70:
    print("Grade B")
elif grade >= 65:
    print("Grade C+")
elif grade >= 65:
    print("Grade C")
elif grade >= 55:
    print("Grade D+")
elif grade >= 50:
    print("Grade D")
else:
    print("Grade F")